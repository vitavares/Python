"""
-> Fateamento: [i:f:p] [::] -> p=passo
-> Fatiamento pelo indice:
 012345678
 olá mundo
-987654321

-> A função len retorna a qtd de caracteres da str
"""

variavel = 'Olá mundo'
print(variavel[2])
print(variavel[4:])
print(variavel[:5])
print(variavel[-9:])
print(variavel[0:9:2]) #Pula de duas em duas letras
print(variavel[::-1])

print(len(variavel))
